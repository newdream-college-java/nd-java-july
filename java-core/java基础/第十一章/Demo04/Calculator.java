package chapter11;

public class Calculator {
	//加
	public double add(double a,double b){
		double sum = 0;
		sum = a +  b;
		return sum;
	}
	//减
	public double minus(double a,double b){
		double sum = 0;
		sum = a - b;
		return sum;
	}
	//乘
	public double multiply(double a,double b){
		double sum = 0;
		sum = a * b;
		return sum;
	}
	//除
	public double divide(double a,double b){
		double sum = 0;
		sum = a / b;
		return sum;
	}
}
