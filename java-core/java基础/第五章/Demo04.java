/*
1.4、循环问“你的名字叫什么？”，如果回答的是“不告诉你”，就继续问，
否则输出“你的名字原来叫：**”。编程实现。
*/
import java.util.Scanner;
public class Demo04{
	public static void main(String[] args){
		Scanner sc = new Scanner(System.in);
		System.out.print("你叫什么名字：");
		String a = sc.next();
		while(a.equals("不告诉你")){
			System.out.print("你叫什么名字：");
			a = sc.next();
		}
		System.out.println("原来你的名字叫："+a);
	}
}